    private void listarProdutosVendidos(){
        try {
            ProdutoDAO produtosdao = new ProdutoDAO();
            
            DefaultTableModel model = (DefaultTableModel) listaProdutos.getModel();
            model.setNumRows(0);
            
            ArrayList<Produto> listagemVendidos = produtosdao.listarProdutosVendidos();
            
            for(int i = 0; i < listagemVendidos.size(); i++){
                model.addRow(new Object[]{
                    listagemVendidos.get(i).getId(),
                    listagemVendidos.get(i).getNome(),
                    listagemVendidos.get(i).getValor()
                });
            }
        } catch (Exception e) {
        }
    
    }
